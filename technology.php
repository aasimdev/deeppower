                    <p>The direct application of the DeepPower Platform is considered to be an Enhanced Geothermal System (EGS). EGS is a next generation geothermal technique and does not require natural hot water reservoirs for the development of geothermal power plants. In EGS, the developer basically creates its own reservoir by drilling down into solid hot rock, injecting water at a high pressure through one well, fracturing the rock to let the water pass through, then collecting the heated water through another well. Theoretically, this approach can create geothermal power plants anywhere in the world by drilling 5 to 6 miles deep.</p>
